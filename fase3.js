const cards = document.querySelectorAll('.memory-card');
let hasFlippedCard = false;
let lockBoard = false;
let firstCard, secondCard;
let matchCounter=0;

function flipCard() {
  if (lockBoard) return;
  if (this === firstCard) return;

  this.classList.add('flip');

  if (!hasFlippedCard) {
    hasFlippedCard = true;
    firstCard = this;

    return;
  }

  secondCard = this;
  checkForMatch();
}

function checkForMatch() {
  let isMatch = firstCard.dataset.autor === secondCard.dataset.autor;
    if (isMatch){
      matchCounter+=1;
	    disableCards();
    if(matchCounter==(cards.length/2)){
      swal({
        title: "Você venceu!",
        text: "Obrigada por jogar",
        type: "success"
    });
     }}
  else{ unflipCards();
	    console.log(firstCard.dataset.autor);
	    console.log(secondCard.dataset.autor);
      }
}
